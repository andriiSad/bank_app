  List<Object> get props => [];
}

class WelcomeState extends AppNavigationStates {
  @override
  List<Object> get props => [];
}

class LoadingState extends AppNavigationStates {
  @override
  List<Object> get props => [];
}

class MainState extends AppNavigationStates {
  @override
  List<Object> get props => [];
}
